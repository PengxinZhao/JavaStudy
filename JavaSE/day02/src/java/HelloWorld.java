//public表示公开
//class表示定义一个类
//HelloWorld表示一个类名
public class HelloWorld {

    /**
     * public表示公开的
     * static表示静态的
     * void表示空
     * main表示方法名
     *(String[] args) 是一个main方法的形式参数列表
     * 需要记住的是：
     *      以下的方法是一个程序的“主方法”，是程序的执行入口
     *      是sun公司规定的，固定编写方式
     * @param args
     */
    public static void main(String[] args) {//表示顶一个公开的静态的主方法
        System.out.println("Hello World!");

        System.out.println("哈喽世界");
    }
}